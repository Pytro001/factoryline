export interface ProductionParams {
  /** Finished units per calendar year (required for throughput math). */
  unitsPerYear: number
  workingDaysPerYear?: number
  shiftsPerDay?: number
  hoursPerShift?: number
  /** Overall equipment effectiveness 0–1, default 0.85 on server. */
  oee?: number
}

export interface ThroughputAnalysis {
  unitsPerYear: number
  workingDaysPerYear: number
  shiftsPerDay: number
  hoursPerShift: number
  oee: number
  taktSec: number
  workedSecondsPerYear: number
  effectiveSecondsPerYear: number
  bottleneckNodeId: string | null
  bottleneckLabel: string
  bottleneckCycleSec: number
  lineTheoreticalUnitsPerYear: number
  meetsTakt: boolean
  parallelStationsSuggested: number
  warnings: string[]
}

export interface FactoryNode {
  id: string
  machineType: string
  label: string
  x: number
  y: number
  width: number
  height: number
  notes?: string
  cycleTimeSec?: number
  isBottleneck?: boolean
}

export interface FactoryEdge {
  id: string
  source: string
  target: string
  label?: string
}

export interface FactoryLayout {
  title: string
  nodes: FactoryNode[]
  edges: FactoryEdge[]
  /** True when layout was generated by built-in demo (no LLM). */
  demo?: boolean
  demoHint?: string
  /** Present when annual volume was provided at generation time. */
  throughput?: ThroughputAnalysis
}

/** Empty canvas for “start from scratch” — add machines from the palette. */
export function createEmptyFactoryLayout(): FactoryLayout {
  return {
    title: 'New line plan',
    nodes: [],
    edges: [],
  }
}

